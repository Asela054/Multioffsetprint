	public function Servicesitemedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_service_details');
        $this->db->where('idtbl_service_details', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_service_details;
        $obj->serviceitem=$respond->row(0)->tbl_service_item_list_idtbl_service_item_list;
		$obj->supplier=$respond->row(0)->tbl_supplier_idtbl_supplier;
		
        echo json_encode($obj);
        
