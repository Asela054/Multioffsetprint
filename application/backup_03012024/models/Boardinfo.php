    public function Boardedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_board');
        $this->db->where('idtbl_board', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_board;
        $obj->name=$respond->row(0)->name;
		$obj->size=$respond->row(0)->tbl_board_size_idtbl_board_size;
		$obj->category=$respond->row(0)->tbl_board_category_idtbl_board_category;
		$obj->unitprice=$respond->row(0)->unitprice;
        echo json_encode($obj);
    }
