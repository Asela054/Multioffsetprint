    public function maintenaceedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_maintenace');
        $this->db->where('idtbl_maintenace', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_maintenace;
        $obj->vehicleregno=$respond->row(0)->tbl_vehicle_idtbl_vehicle;
        $obj->servicecharge=$respond->row(0)->service_charge;
        $obj->comments=$respond->row(0)->comments;
      
    
        echo json_encode($obj);
    }
