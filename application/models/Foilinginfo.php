    public function Foilingedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_foiling');
        $this->db->where('idtbl_foiling', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_foiling;
        $obj->foiling=$respond->row(0)->foiling;
		$obj->perinch_cost=$respond->row(0)->perinch_cost;
        echo json_encode($obj);
    }
