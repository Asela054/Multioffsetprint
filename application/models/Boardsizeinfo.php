    public function Boardsizeedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_board_size');
        $this->db->where('idtbl_board_size', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_board_size;
        $obj->width=$respond->row(0)->width;
		$obj->length=$respond->row(0)->length;
		$obj->name=$respond->row(0)->name;
        echo json_encode($obj);
    }
